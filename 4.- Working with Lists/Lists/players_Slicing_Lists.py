#slicing list
players= ['Ana', 'Maria', 'Pedro', 'Juana', 'Carlos', 'Petra','Carol', 'Jimena','Nati']

print(f"Lista Original {players}")
print(f" Lista por rango (0:3): {players[0:3]}")
print(f" Lista por rango (:4): {players[:4]}")
print(f" Lista por rango (1:): {players[1:]}")
print(f" Lista por rango (-3:): {players[-3:]}")
print(f" Lista por rango (:-2): {players[:-2]}")
print(f" Lista por rango (0:7:2): {players[0:7:2]}")